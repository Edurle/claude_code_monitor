# в”Җв”Җв”Җ й…ҚзҪ®еҠ иҪҪ в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ

class BannerConfig:
    """Banner й…ҚзҪ®"""
    __slots__ = ("enabled", "text", "font", "color", "alignment")

    def __init__(self, enabled: bool = True, text: str = "Claude Home",
                 font: str = "slant", color: str = "primary",
                 alignment: str = "center"):
        self.enabled = enabled
        self.text = text
        self.font = font
        self.color = color
        self.alignment = alignment

    @classmethod
    def from_yaml(cls, path: Path) -> "BannerConfig":
        """д»Һ YAML ж–Үд»¶еҠ иҪҪй…ҚзҪ®пјҲйӣ¶дҫқиө–и§Јжһҗпјү"""
        cfg = cls()
        if not path.exists():
            return cfg

        in_banner = False
        try:
            with open(path, "r", encoding="utf-8") as f:
                for line in f:
                    stripped = line.strip()
                    if stripped.startswith("#") or not stripped:
                        continue
                    if stripped.startswith("banner:"):
                        in_banner = True
                        continue
                    # жЈҖжөӢжҳҜеҗҰзҰ»ејҖ banner еқ—пјҲж–°зҡ„йЎ¶еұӮй”®пјү
                    if in_banner and not line.startswith(" ") and not line.startswith("\t"):
                        in_banner = False
                    if not in_banner:
                        continue
                    # и§Јжһҗ key: value
                    if ":" in stripped:
                        key, _, val = stripped.partition(":")
                        key = key.strip()
                        val = val.strip().strip('"').strip("'")
                        if key == "enabled":
                            cfg.enabled = val.lower() in ("true", "yes", "1")
                        elif key == "text":
                            cfg.text = val
                        elif key == "font":
                            cfg.font = val if val in FONTS else "slant"
                        elif key == "color":
                            cfg.color = val
                        elif key == "alignment":
                            cfg.alignment = val
        except Exception:
            pass
        return cfg


# в”Җв”Җв”Җ жёІжҹ“еҷЁ в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ

class BannerRenderer:
    """ASCII art banner жёІжҹ“еҷЁ"""

    def __init__(self, config_path: Path):
        self._config_path = config_path
        self.config = BannerConfig.from_yaml(config_path)
        # зј“еӯҳ: (text, font, width) -> List[str]
        self._cache_key = None
        self._cache_lines = []

    @property
    def enabled(self) -> bool:
        return self.config.enabled

    def render(self, width: int, compact: bool = False) -> List[str]:
        """жёІжҹ“ bannerпјҢиҝ”еӣһжҜҸиЎҢеӯ—з¬ҰдёІеҲ—иЎЁ

        Args:
            width: еҸҜз”Ёе®ҪеәҰпјҲеҲ—ж•°пјү
            compact: зҙ§еҮ‘жЁЎејҸпјҢејәеҲ¶дҪҝз”Ё mini еӯ—дҪ“
        """
        if not self.config.enabled:
            return []

        text = self.config.text
        if not text:
            return []

        # йҖүжӢ©еӯ—дҪ“
        if compact:
            font_name = "mini"
        else:
            font_name = self._pick_font(text, width)

        # зј“еӯҳжЈҖжҹҘ
        cache_key = (text, font_name, width)
        if self._cache_key == cache_key:
            return self._cache_lines

        if font_name is None:
            # е…ЁйғЁеӯ—дҪ“йғҪеӨӘе®ҪпјҢйҷҚзә§дёәзәҜж–Үжң¬
            lines = self._render_plain(text, width)
        else:
            lines = self._render_font(text, font_name, width)

        self._cache_key = cache_key
        self._cache_lines = lines
        return lines

    def get_height(self, width: int, compact: bool = False) -> int:
        """иҝ”еӣһ banner еҚ з”ЁиЎҢж•°"""
        return len(self.render(width, compact))

    def _pick_font(self, text: str, width: int) -> Optional[str]:
        """йҖүжӢ©йҖӮеҗҲе®ҪеәҰзҡ„еӯ—дҪ“пјҢиҝ”еӣһ None иЎЁзӨәйңҖйҷҚзә§дёәзәҜж–Үжң¬"""
        # дјҳе…ҲдҪҝз”Ёз”ЁжҲ·й…ҚзҪ®зҡ„еӯ—дҪ“
        preferred = self.config.font
        order = FONT_FALLBACK.copy()
        if preferred in order:
            order.remove(preferred)
            order.insert(0, preferred)

        for name in order:
            font = FONTS[name]
            art_width = self._measure_width(text, font)
            if art_width <= width:
                return name
        return None

    def _measure_width(self, text: str, font: dict) -> int:
        """жөӢйҮҸж–Үжң¬еңЁжҢҮе®ҡеӯ—дҪ“дёӢзҡ„жёІжҹ“е®ҪеәҰ"""
        total = 0
        for ch in text:
            key = ch
            if key not in font and key.isalpha():
                key = ch.upper()
            if key in font:
                total += max(len(l) for l in font[key])
            else:
                total += 1  # жңӘзҹҘеӯ—з¬ҰжҢү1еҲ—з®—
        return total

    def _render_font(self, text: str, font_name: str, width: int) -> List[str]:
        """з”ЁжҢҮе®ҡеӯ—дҪ“жёІжҹ“ж–Үжң¬"""
        font = FONTS[font_name]
        upper_only = font_name in _ONLY_UPPER_FONTS

        # ж”¶йӣҶжҜҸдёӘеӯ—з¬Ұзҡ„ art иЎҢ
        char_arts = []
        for ch in text:
            key = ch.upper() if (upper_only and ch.isalpha()) else ch
            if key not in font:
                key = " "  # еӣһйҖҖеҲ°з©әж ј
            char_arts.append(font[key])

        if not char_arts:
            return []

        # ж°ҙе№іжӢјжҺҘ
        height = len(char_arts[0])
        lines = []
        for row in range(height):
            line = "".join(art[row] for art in char_arts)
            # иЈҒеүӘеҲ°е®ҪеәҰ
            lines.append(line[:width])

        # еұ…дёӯ/еҜ№йҪҗ
        lines = self._align(lines, width)

        return lines

    def _render_plain(self, text: str, width: int) -> List[str]:
        """зәҜж–Үжң¬йҷҚзә§пјҡеҚ•иЎҢжҳҫзӨә"""
        bar = f"  {text}  "
        if len(bar) > width:
            bar = bar[:width]
        line = self._align_single(bar, width)
        return [line]

    def _align(self, lines: List[str], width: int) -> List[str]:
        """еҜ№дёҖз»„иЎҢеә”з”ЁеҜ№йҪҗ"""
        alignment = self.config.alignment
        if alignment == "center":
            max_len = max(len(l) for l in lines) if lines else 0
            pad = max(0, (width - max_len) // 2)
            return [" " * pad + l for l in lines]
        elif alignment == "right":
            return [l.rjust(width) for l in lines]
        return lines

    def _align_single(self, line: str, width: int) -> str:
        """еҜ№еҚ•иЎҢеә”з”ЁеҜ№йҪҗ"""
        alignment = self.config.alignment
        if alignment == "center":
            return line.center(width)
        elif alignment == "right":
            return line.rjust(width)
        return line
